class CupertinoSpacing {
  CupertinoSpacing._();

  /// Standard screen edge margin (16.0pt)
  static const double screenMargin = 16.0;

  /// Half screen edge margin (8.0pt)
  static const double halfScreenMargin = 8.0;

  // Layout spacers / padding sizes
  static const double xs = 4.0;
  static const double s = 8.0;
  static const double m = 12.0;
  static const double l = 16.0;
  static const double xl = 20.0;
  static const double xxl = 24.0;
  static const double xxxl = 32.0;

  /// Minimum Apple Human Interface Guidelines touch target size (44.0pt)
  static const double minTouchTarget = 44.0;

  /// Standard height for primary action buttons (48.0pt)
  static const double primaryButtonHeight = 48.0;

  /// Standard height for standard buttons or text inputs (44.0pt)
  static const double standardControlHeight = 44.0;

  /// Typical iOS rounded corner radius for grouped layouts or cards (10.0pt)
  static const double cardRadius = 10.0;

  /// Standard iOS button corner radius (8.0pt)
  static const double buttonRadius = 8.0;

  /// Standard iOS sheet/dialog corner radius (14.0pt)
  static const double dialogRadius = 14.0;
}
